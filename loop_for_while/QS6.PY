## WAP TO PRINT FIRST 10 NATURAL NUMBER IN REVERSE ORDER USING WHILE NUMBER:

n = int(input("Enter any number: "))
n1 = 1
step = -1
for i in range(n, n1 - 1, step):
    print(i, end=" ")


